package com.po4yka.bitesizereader.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Standardized icon sizes for consistent iconography throughout the app.
 */
object IconSizes {
    /** 16.dp - Small indicators, badges, read status icons */
    val xs = 16.dp

    /** 20.dp - Secondary icons, inline icons */
    val sm = 20.dp

    /** 24.dp - Primary action icons in toolbars and headers */
    val md = 24.dp

    /** 32.dp - Emphasis icons, loading spinners */
    val lg = 32.dp

    /** 80.dp - Large icons for empty states */
    val xl = 80.dp
}
