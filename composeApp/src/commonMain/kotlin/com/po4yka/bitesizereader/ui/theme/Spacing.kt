package com.po4yka.bitesizereader.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing scale for consistent padding and margins throughout the app.
 * Based on a 4dp grid system.
 */
object Spacing {
    /** 4.dp - Tight spacing for compact elements */
    val xxs = 4.dp

    /** 8.dp - Internal component spacing */
    val xs = 8.dp

    /** 12.dp - Item spacing in lists */
    val sm = 12.dp

    /** 16.dp - Standard content padding */
    val md = 16.dp

    /** 24.dp - Section spacing */
    val lg = 24.dp

    /** 32.dp - Large gaps between major sections */
    val xl = 32.dp

    /** 48.dp - Extra large spacing for empty states */
    val xxl = 48.dp
}
